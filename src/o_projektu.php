			<h1>O projektu</h1>
			
			
			
			<p class="perex">Cílem výstavy je multimediální, interaktivní a především atraktivní formou představit Fráni Šrámka a jeho dílo a přiblížit návštěvníkům toto mnohdy vzdálené období literatury. Podstatnou součástí projektu jsou i <a href="doprovodne-akce">doprovodné programy</a> a <a href="workshopy">workshopy</a>, díky nimž se muzeum stane inspirativním prostorem, kde lze i něco zažít.</p>
			
			<h2>Pro studenty</h2>
			<p>Nová výstava je jen jednou z částí projektu Revitalizace muzea Fráni Šrámka v Sobotce – řeč je právě o <a href="doprovodne-akce">doprovodných programech</a>. Žáci a studenti základních i středních škol se utkají v literární soutěži s tématem „Vzdor“ nebo v soutěži filmové, jejíž tématem je název Šrámkovy básnické sbírky „Života bído, přec tě mám rád…!“ Stačí jen vymyslet příběh! V rámci výstavy se uskuteční i workshopy, ve kterých se žáci ZŠ Sobotka a studenti Gymnázi Dr. Emila Holuba v Holicích dozví, jak lze využít literaturu v praxi, seznámí se s mladými autory, naučí se psát filmový scénář a zkusí si natočit a sestříhat film. Celý projekt doplní školní výuku literatury a českého jazyka.
			</p>
			<p>Ostatní návštěvníky, kteří již znají Šrámkovo dílo, může zaujmout především audioinstalace, básně ilustrované fotografiemi a představení literárních směrů. I oni si budou moci v pracovním listu, který je součástí informační brožurky k výstavě, ověřit své znalosti.</p>
			
			<h2>Realizace projektu</h2>
			
			<p>Projekt Revitalizace muzea Fráni Šrámka v Sobotce vznikl v rámci magisterské diplomové práce Niny Seyčkové na Filosofické fakultě Univerzity Karlovy na oboru <a href="http://novamedia.ff.cuni.cz/">Studia nových médií</a>. Za projektem dále stojí komiksová kreslířka TOY_BOX, autorka životopisné komiksové instalace a ostatních kreseb, grafickou podobu výstavy připravil Jonáš Kocián, webovou prezentaci vytvořil David Birke. Poděkování patří PhDr. Pavlu Šidákovi, Ph.D. za zkontrolování faktické správnosti literárních exponátů, Marii Sekerové a Karolu Bílkovi za odborné konzultace, Lukáši Seyčkovi a Karlu Šeblovi za pomoc při instalaci výstavy, Lee Vejclové a Janu Janatkovi a všem, kteří mě během přípravy projektu podporovali.</p>
			
