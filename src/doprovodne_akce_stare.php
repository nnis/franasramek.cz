				<h2>Vzdor</h2>
				<p>Literární soutěž je uspořádána pro jednotlivce v kategorii základní školy a v kategorii střední školy a gymnázia. Přihlásit se může kdokoli s textem, povídkou, úvahou, esejí, básní, komiksem či jiným literárním žánrem — jediné, co je třeba dodržet je téma a rozsah (text nesmí přesáhnout 10 normostran, tedy 18 000 znaků).</p>
				<p>Tématem literární soutěže je „Vzdor“ — každý se někdy ocitneme v situaci, kdy s něčím nejsme spokojeni, kdy bychom chtěli něco změnit, kdy sebereme síly a začneme být vzpurní, vzbouříme se proti autoritě, nespravedlnosti nebo jakémukoli jinému bezpráví. Zkuste využít svého pocitu vzdoru, vymyslete příběh a napište jej. Práce budou uveřejněny na stránkách muzea, nejlepší z nich budou oceněny a jejich autoři budou mít možnost veřejně přečíst před publikem svá díla na Festivalu Šrámkova Sobotka v červenci 2012.</p>
				
